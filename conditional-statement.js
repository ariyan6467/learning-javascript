//conditional statement (if,if-else,if-else-if)


// if statement
let mahi = 2;
if (mahi >= 18) {
  console.log("Adult");
} 
if (mahi < 18) {
  console.log("Not Adult");
}

// if-else statement
let numphy = 10;
if(numphy % 2 == 0) {
  console.log("Even Number");
} else {
  console.log("Odd Number");
}




// if-else-if statement
let num = 10;
if (num > 0) {
  console.log("Positive");
} else if (num < 0) {
  console.log("Negative");
} else {
  console.log("Zero");
}